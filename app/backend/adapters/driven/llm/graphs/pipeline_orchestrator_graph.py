"""
Pipeline Orchestrator Agent

Main orchestrator agent that manages the entire document processing pipeline
using LangGraph. Replaces the Event-Driven architecture with a unified agent
that has full visibility and control over all stages.

Features:
- State management with checkpoints (recovery on crash)
- Legacy data validation (reads old data, compares with new, merges)
- Observability (persists events at each stage)
- Intelligent decisions (based on previous results)
- Sub-agents as tools (ValidationService, OCRService, SegmentationAgent, etc.)

Related: REQ-027_ORCHESTRATOR_MIGRATION.md, AGENT_ORCHESTRATION_ARCHITECTURE.md
Date: 2026-04-10
"""

