def build_orchestrator_workflow(db_pool, legacy_repo: LegacyDataRepository) -> StateGraph:
    """
    Build the complete Pipeline Orchestrator Agent workflow.
    
    Args:
        db_pool: AsyncPG connection pool for DB operations
        legacy_repo: Repository for legacy data operations
    
    Returns:
        Compiled LangGraph workflow with legacy validation at each stage
    """
    workflow = StateGraph(OrchestratorState)
    
    # Add nodes (processing + legacy adapter after each)
    workflow.add_node("check_legacy", lambda state: check_if_legacy_node(state, db_pool))
    workflow.add_node("validation", lambda state: validation_node(state, db_pool))
    workflow.add_node("legacy_adapter_validation", lambda state: legacy_adapter_node(state, db_pool, legacy_repo))
    workflow.add_node("ocr", lambda state: ocr_node(state, db_pool))
    workflow.add_node("legacy_adapter_ocr", lambda state: legacy_adapter_node(state, db_pool, legacy_repo))
    workflow.add_node("segmentation", lambda state: segmentation_node(state, db_pool))
    workflow.add_node("legacy_adapter_segmentation", lambda state: legacy_adapter_node(state, db_pool, legacy_repo))
    workflow.add_node("chunking", lambda state: chunking_node(state, db_pool))
    workflow.add_node("legacy_adapter_chunking", lambda state: legacy_adapter_node(state, db_pool, legacy_repo))
    workflow.add_node("indexing", lambda state: indexing_node(state, db_pool))
    workflow.add_node("legacy_adapter_indexing", lambda state: legacy_adapter_node(state, db_pool, legacy_repo))
    workflow.add_node("insights", lambda state: insights_node(state, db_pool))
    workflow.add_node("legacy_adapter_insights", lambda state: legacy_adapter_node(state, db_pool, legacy_repo))
    
    # Define flow with legacy validation after each stage
    workflow.set_entry_point("check_legacy")
    
    # Flow: check → validation → [legacy adapter] → ocr → [legacy adapter] → segmentation → [legacy adapter] → END
    workflow.add_edge("check_legacy", "validation")
    
    # Conditional: Skip legacy adapter if not in migration mode
    def should_run_legacy_adapter(state: OrchestratorState) -> str:
        return "legacy_adapter_validation" if state.get('migration_mode') else "ocr"
    
    workflow.add_conditional_edges(
        "validation",
        should_run_legacy_adapter,
        {
            "legacy_adapter_validation": "legacy_adapter_validation",
            "ocr": "ocr"
        }
    )
    
    workflow.add_edge("legacy_adapter_validation", "ocr")
    
    # OCR → legacy adapter
    def should_run_legacy_adapter_ocr(state: OrchestratorState) -> str:
        return "legacy_adapter_ocr" if state.get('migration_mode') else "segmentation"
    
    workflow.add_conditional_edges(
        "ocr",
        should_run_legacy_adapter_ocr,
        {
            "legacy_adapter_ocr": "legacy_adapter_ocr",
            "segmentation": "segmentation"
        }
    )
    
    workflow.add_edge("legacy_adapter_ocr", "segmentation")
    
    # Segmentation → legacy adapter → chunking
    def should_run_legacy_adapter_segmentation(state: OrchestratorState) -> str:
        return "legacy_adapter_segmentation" if state.get('migration_mode') else "chunking"
    
    workflow.add_conditional_edges(
        "segmentation",
        should_run_legacy_adapter_segmentation,
        {
            "legacy_adapter_segmentation": "legacy_adapter_segmentation",
            "chunking": "chunking"
        }
    )
    
    workflow.add_edge("legacy_adapter_segmentation", "chunking")
    
    # Chunking → legacy adapter → indexing
    def should_run_legacy_adapter_chunking(state: OrchestratorState) -> str:
        return "legacy_adapter_chunking" if state.get('migration_mode') else "indexing"
    
    workflow.add_conditional_edges(
        "chunking",
        should_run_legacy_adapter_chunking,
        {
            "legacy_adapter_chunking": "legacy_adapter_chunking",
            "indexing": "indexing"
        }
    )
    
    workflow.add_edge("legacy_adapter_chunking", "indexing")
    
    # Indexing → legacy adapter → insights
    def should_run_legacy_adapter_indexing(state: OrchestratorState) -> str:
        return "legacy_adapter_indexing" if state.get('migration_mode') else "insights"
    
    workflow.add_conditional_edges(
        "indexing",
        should_run_legacy_adapter_indexing,
        {
            "legacy_adapter_indexing": "legacy_adapter_indexing",
            "insights": "insights"
        }
    )
    
    workflow.add_edge("legacy_adapter_indexing", "insights")
    
    # Insights → legacy adapter → END
    def should_run_legacy_adapter_insights(state: OrchestratorState) -> str:
        return "legacy_adapter_insights" if state.get('migration_mode') else "end"
    
    workflow.add_conditional_edges(
        "insights",
        should_run_legacy_adapter_insights,
        {
            "legacy_adapter_insights": "legacy_adapter_insights",
            "end": END
        }
    )
    
    workflow.add_edge("legacy_adapter_insights", END)
    
    # Compile with checkpointing for recovery
    # TODO: Use persistent SQLite checkpoint for production
    # from langgraph.checkpoint.sqlite import SqliteSaver
    # memory = SqliteSaver.from_conn_string("checkpoints.db")
    # return workflow.compile(checkpointer=memory)
    
    return workflow.compile()


