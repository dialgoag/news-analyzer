class PipelineOrchestratorAgent:
    """
    Main Pipeline Orchestrator Agent.
    
    Manages the entire document processing pipeline with:
    - Legacy validation
    - Observability
    - Recovery
    - Intelligent decisions
    """
    
    def __init__(self, db_pool):
        self.db_pool = db_pool
        self.legacy_repo = LegacyDataRepository(db_pool)
        self.workflow = build_orchestrator_workflow(db_pool, self.legacy_repo)
    
    async def process_document(
        self,
        document_id: str,
        filename: str,
        filepath: str
    ) -> Dict[str, Any]:
        """
        Process a complete document through the pipeline.
        
        Args:
            document_id: Document UUID/ID
            filename: Original filename
            filepath: Path to PDF file
        
        Returns:
            Final state with results
        """
        logger.info(f"🚀 Starting orchestrator for document: {document_id} ({filename})")
        
        # Initialize state
        initial_state: OrchestratorState = {
            'document_id': document_id,
            'filename': filename,
            'filepath': filepath,
            'metadata': {},
            'pipeline_context': {},
            'current_stage': None,
            'migration_mode': False,
            'legacy_data': {},
            'new_data': {},
            'validation_results': {},
            'merged_data': {},
            'events': [],
            'errors': [],
            'skip_insights': False,
            'retry_ocr_with_tika': False
        }
        
        try:
            # Execute workflow
            final_state = await self.workflow.ainvoke(initial_state)
            
            logger.info(f"✅ Orchestrator completed for document: {document_id}")
            
            return {
                'success': True,
                'document_id': document_id,
                'final_state': final_state,
                'errors': final_state.get('errors', [])
            }
        
        except Exception as e:
            logger.error(f"❌ Orchestrator failed for document {document_id}: {e}", exc_info=True)
            
            return {
                'success': False,
                'document_id': document_id,
                'error': str(e),
                'traceback': traceback.format_exc()
            }


