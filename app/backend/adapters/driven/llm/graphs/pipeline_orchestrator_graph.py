class OrchestratorState(TypedDict):
    """
    Shared state for the entire pipeline orchestrator.
    
    This state is passed between all nodes and provides:
    - Full visibility of all previous results
    - Context for intelligent decisions
    - Legacy data for validation
    - Events for observability
    """
    # Document info
    document_id: str
    filename: str
    filepath: str
    
    # Metadata (extracted from filename or legacy)
    metadata: Dict[str, Any]  # {publication_date, newspaper, sha8, etc.}
    
    # Pipeline context (results from each stage)
    pipeline_context: Dict[str, Any]  # {ocr: {...}, segmentation: {...}, etc.}
    
    # Current stage tracking
    current_stage: Optional[str]
    
    # Migration mode (True if document is legacy)
    migration_mode: bool
    
    # Legacy data (for validation)
    legacy_data: Dict[str, Any]  # {stage: LegacyData}
    
    # New data (generated by orchestrator)
    new_data: Dict[str, Any]  # {stage: NewData}
    
    # Validation results (legacy vs new)
    validation_results: Dict[str, Any]  # {stage: ValidationResult}
    
    # Merged data (final result)
    merged_data: Dict[str, Any]  # {stage: MergedData}
    
    # Events timeline (for observability)
    events: List[Dict[str, Any]]
    
    # Errors encountered
    errors: List[Dict[str, Any]]
    
    # Flags for flow control
    skip_insights: bool
    retry_ocr_with_tika: bool


