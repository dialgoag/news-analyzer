class PipelineStage(str, Enum):
    """Pipeline stages for migration tracking"""
    UPLOAD = "upload"
    VALIDATION = "validation"
    OCR = "ocr"
    OCR_VALIDATION = "ocr_validation"
    SEGMENTATION = "segmentation"
    CHUNKING = "chunking"
    INDEXING = "indexing"
    INSIGHTS = "insights"
    INDEXING_INSIGHTS = "indexing_insights"


class ValidationStatus(str, Enum):
    """Status of legacy vs new data validation"""
    PENDING = "pending"
    MATCH = "match"
    MISMATCH = "mismatch"
    CONFLICT = "conflict"
    NO_LEGACY = "no_legacy"


class MergeStrategy(str, Enum):
    """Strategy for merging legacy and new data"""
    KEEP_NEW = "keep_new"
    KEEP_LEGACY = "keep_legacy"
    MERGE_BOTH = "merge_both"
    MANUAL_REVIEW = "manual_review"


class MigrationStatus(str, Enum):
    """Overall migration status for a document"""
    PENDING = "pending"
    IN_PROGRESS = "in_progress"
    VALIDATED = "validated"
    COMPLETED = "completed"


class DataSource(str, Enum):
    """Source of pipeline data"""
    LEGACY = "legacy"
    ORCHESTRATOR = "orchestrator"


