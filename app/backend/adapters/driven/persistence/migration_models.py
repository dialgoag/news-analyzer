def calculate_similarity(legacy: Dict[str, Any], new: Dict[str, Any], stage: PipelineStage) -> float:
    """
    Calculate similarity between legacy and new data.
    Strategy depends on stage.
    
    Returns: float 0.0 - 1.0
    """
    if stage == PipelineStage.OCR:
        # Text similarity (e.g., using difflib or Levenshtein)
        legacy_text = legacy.get('text', '')
        new_text = new.get('text', '')
        
        if not legacy_text or not new_text:
            return 0.0
        
        # Simple similarity (could use more sophisticated algorithm)
        from difflib import SequenceMatcher
        return SequenceMatcher(None, legacy_text, new_text).ratio()
    
    elif stage == PipelineStage.SEGMENTATION:
        # Article count similarity
        legacy_count = len(legacy.get('articles', []))
        new_count = len(new.get('articles', []))
        
        if legacy_count == 0 and new_count == 0:
            return 1.0
        
        max_count = max(legacy_count, new_count)
        if max_count == 0:
            return 1.0
        
        return 1.0 - abs(legacy_count - new_count) / max_count
    
    else:
        # Generic similarity (exact match check)
        return 1.0 if legacy == new else 0.5


def determine_merge_strategy(validation: ValidationResult) -> MergeStrategy:
    """
    Determine merge strategy based on validation result.
    """
    if validation.status == ValidationStatus.NO_LEGACY:
        return MergeStrategy.KEEP_NEW
    
    if validation.status == ValidationStatus.MATCH:
        return MergeStrategy.KEEP_NEW  # New is validated, use it
    
    if validation.status == ValidationStatus.CONFLICT:
        return MergeStrategy.MANUAL_REVIEW
    
    if validation.similarity_score and validation.similarity_score >= 0.95:
        return MergeStrategy.KEEP_NEW  # High similarity, trust new
    
    if validation.similarity_score and validation.similarity_score < 0.80:
        return MergeStrategy.MANUAL_REVIEW  # Low similarity, needs review
    
    return MergeStrategy.MERGE_BOTH  # Medium similarity, merge
