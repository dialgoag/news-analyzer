    """
    Data from legacy Event-Driven pipeline.
    Snapshot of old data for a specific stage.
    """
    stage: PipelineStage
    exists: bool = Field(..., description="Whether legacy data exists for this stage")
    data: Dict[str, Any] = Field(default_factory=dict, description="Legacy data snapshot")
    timestamp: Optional[datetime] = Field(None, description="When legacy data was created")
    source_table: Optional[str] = Field(None, description="Source table (document_status, news_items, etc.)")
    
    class Config:
        use_enum_values = True


