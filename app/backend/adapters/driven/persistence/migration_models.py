class DocumentMetadata(BaseModel):
    """
    Metadata extracted from document filename.
    Pattern: {sha8}_{DD-MM-YY}-{Newspaper}.pdf
    """
    sha8: str = Field(..., min_length=8, max_length=8, description="First 8 chars of SHA256")
    date: datetime = Field(..., description="Publication date")
    newspaper: str = Field(..., min_length=1, description="Newspaper name")
    is_valid: bool = Field(True, description="Whether parsing was successful")
    original_filename: Optional[str] = None
    
    @validator('sha8')
    def validate_sha8_hex(cls, v):
        """Validate that sha8 is valid hexadecimal"""
        try:
            int(v, 16)
        except ValueError:
            raise ValueError(f"sha8 must be valid hexadecimal, got: {v}")
        return v.lower()


class DocumentMigrationStatus(BaseModel):
    """
    Migration status for a complete document.
    Summary across all stages.
    """
    document_id: UUID
    filename: str
    publication_date: Optional[datetime] = None
    newspaper_name: Optional[str] = None
    
    # Migration status
    data_source: DataSource
    migration_status: MigrationStatus
    migrated_at: Optional[datetime] = None
    
    # Stage progress
    stages_tracked: int = 0
    stages_migrated: int = 0
    pending_stages: List[str] = Field(default_factory=list)
    
    @property
    def migration_percent(self) -> float:
        """Calculate migration percentage"""
        if self.stages_tracked == 0:
            return 0.0
        return (self.stages_migrated / self.stages_tracked) * 100.0
    
    class Config:
        use_enum_values = True


